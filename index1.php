		<p>The IIT JEE is an entrance test conducted by the Indian Institute of Technology (IIT) for admission to the most prestiguos and reputed engineering colleges of India - the Indian Institute of Technology (IIT) , Institute of Tehnology - Benares Hindu University (IT-BHU) and the Indian School of Mines, Dhanbad (ISM-D). It was the first test to adopt a two-tier system as early as 2000 with the screening test in addition to the main exam. IIT JEE is more of a rejection test than a selection test as more candidates are rejected rather than selected. Nearly 3 x 105 students sit for the IIT JEE Screening Test, the first of the two tests out of which only 25000 or so candidates qualify for the IIT JEE Main examination, the second and last of the two tests. From the IIT JEE Main examination, only 4000 or so students get to qualify for the IITs and get a seat in the IITs. So high is the competition that only 4000 out of 3 x 105 IIT JEE Aspirants get to call themselves IITians.</p><P>
		The IIT JEE Screening test contains 84 multiple choice type questions, in which only one answer out of the four given answers is correct. Each question is of 3 marks and the Maximum Marks are 252. The time for the IIT JEE Screening test is from 10:30 AM to 1:30 PM. For every right answer, the student is awarded 3 marks, but for every wrong answer, One mark is deducted. No marks are awarded or deducted for unanswered questions. This Negative-marking system was done to leave no scope for tukkafication. Besides the IIT JEE, many other engineering enterance examinations have started to use this format. In tukkafication, a student just has a chance of getting One mark per 4 questions attempted if there were no negative marking, taking the probability of answering correctly as 25%. But with negative marking, he gets 0 out of 12 marks in 4 out of the 4 questions attempted, assuming a probability of 25% for a correct answer. This negative-marking system leaves him in a more worser situation than where he was before 		attempting tukkafication - at 0, but with 4 less questions remaining. </p>
		<p>The Screening test for the IIT JEE 2000 was held in January 2000. For the IIT JEE 2001 and 2002, it was held in December of the previous year. The Screening test for IIT JEE 2003 was held in April 2003 and the most expected timing of the IIT JEE 2004 Screening test looks to be in April 2004. From the IIT JEE 2003, the syllabus for the IIT JEE Screening test and the IIT JEE main examination has been made the same. The IIT JEE Screening test not only checks the academic strength of the student, but also his mental strength. Infact it is a 50% test of the students academic strength and 50% test of his mental strength. </p><p>
		In the main exam, 3 tests, each of 2 hours are conducted for Chemistry, Physics and Mathematics. It contains 12 questions each of 5 marks, the maximum marks being 60. The time for the IIT JEE Main examination is from 8 AM to 10 AM for Chemistry, 11:30 AM to 1:30 PM for Physics and 3:30 PM to 5:30 PM for Mathematics. There are NO multiple choice questions or Objective questions asked in the main examination. All the questions asked in the IIT JEE Main examination are fully subjective. The toppers of IIT JEE exam and the selection for IIT is decided on the basis of the students performance and Marks obtained in the main exam. In case of 2 or more students obtaining the same marks, cut-off method is used for finding the rank. From the cut-off method, the student who obtains the highest marks in the subject with the least cut-off marks gets a higher rank. </p>


